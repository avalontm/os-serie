#pragma once

#include "stdlib/types.h"

// ------------------------------------------------------------------------------------------------
static inline u16 NetSwap16(u16 x)
{
    return (x>>8) | (x<<8);
}

// ------------------------------------------------------------------------------------------------
static inline u32 NetSwap32(u32 x)
{
    return __builtin_bswap32(x);
}

// ------------------------------------------------------------------------------------------------
static inline u64 NetSwap64(u64 x)
{
    return __builtin_bswap64(x);
}
